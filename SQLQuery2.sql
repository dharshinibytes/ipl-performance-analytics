CREATE TABLE Deliveries (
		match_id INT  ,
		inning INT,
		batting_team VARCHAR(50),
		bowling_team VARCHAR(50),
		[over] INT,
		ball INT,
		batter VARCHAR (50),
		bowler VARCHAR(50),
		non_striker VARCHAR(50),
		batsman_runs int,
		extra_runs INT,
		total_runs INT,
		extras_type VARCHAR(50),
		is_wicket INT,
		player_dismissed VARCHAR(50),
		dismissal_kind VARCHAR(50),
		fielder VARCHAR(50)
);
